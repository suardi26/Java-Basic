package com.practice;

public class Main {

    public static void main (String[] args){
        //Operator Assignment adalah Operator yang digunakan untuk memanipulasi serta menginisialisasi nilai pada sebuah variabel.
        //Operator Assignment identik dengan simbol '=' dan dapat digabungkan dengan simbol operator aritmatika.contohnya : '+=','-=','*=','/=','%/'.

        //Operator Assignment Penjumlahan.
        int A = 20;
        A += 30; // A = A + 30;
        System.out.println("Nilai Variabel 'A' = "+A);

        //Operator Assignment Pengurangan.
        int B = 50;
        B -= 20;
        System.out.println("Nilai Variabel 'B' = "+B);

        //Operator Assigment Perkalian.
        int C = 20;
        C *= 3;
        System.out.println("Nilai Variabel 'C' = "+C);

        //Operator Assigment Pembagian.
        int D = 20;
        D /= 4;
        System.out.println("Nilai variabel 'D' = "+D);

        //Operator Assignment Modulus.
        int E = 22;
        E %= 4;
        System.out.println("Nilai Variabel 'E' = "+E);

    }
}
